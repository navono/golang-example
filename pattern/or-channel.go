package pattern

import (
	"fmt"
	"github.com/urfave/cli"
	"time"
)

func orAction(c *cli.Context) error {
	orPattern()
	return nil
}

func orPattern() {
	var or func(channels ...<-chan interface{}) <-chan interface{}
	or = func(channels ...<-chan interface{}) <-chan interface{} {
		switch len(channels) {
		// 递归的退出条件
		case 0:
			return nil
		case 1:
			return channels[0]
		}

		orDone := make(chan interface{})
		go func() {
			defer close(orDone)
			switch len(channels) {
			case 2:
				select {
				case <-channels[0]:
				case <-channels[1]:
				}
			default:
				select {
				case <-channels[0]:
				case <-channels[1]:
				case <-channels[2]:
				case <-or(append(channels[3:], orDone)...):
				}
			}
		}()

		return orDone
	}

	sig := func(after time.Duration) <-chan interface{} {
		c := make(chan interface{})
		go func() {
			defer close(c)
			time.Sleep(after)
		}()
		return c
	}

	start := time.Now()
	<-or(
		sig(2*time.Hour),
		sig(1*time.Minute),
		sig(1*time.Second),
		sig(2*time.Hour),
		sig(5*time.Minute),
	)

	fmt.Printf("Done after: %v", time.Since(start))
}
